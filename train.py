class EuroSATDataset(Dataset):
    """
    Reads a CSV with columns:
        image_id  — filename or relative path  (e.g. AnnualCrop/AnnualCrop_00001.jpg)
        label     — integer class index (absent in test set)

    Set `has_labels=False` for the test split.
    """

    def __init__(self, csv_path, dataset_dir, transform=None, has_labels=True):
        self.df          = pd.read_csv(csv_path)
        self.dataset_dir = dataset_dir
        self.transform   = transform
        self.has_labels  = has_labels

        # ══ normalise column names ═════════════════════
        self.df.columns = [c.strip().lower() for c in self.df.columns]

        # accept either 'image_id' or 'filename'
        if "image_id" in self.df.columns:
            self.img_col = "image_id"
        elif "filename" in self.df.columns:
            self.img_col = "filename"
        else:
            raise ValueError(f"CSV must have 'image_id' or 'filename' column. Found: {list(self.df.columns)}")

        # Filter out missing classes based on actual directories present
        actual_class_dirs = [d for d in os.listdir(dataset_dir) if os.path.isdir(os.path.join(dataset_dir, d))]
        self.df = self.df[self.df[self.img_col].apply(lambda x: x.split('/')[0] in actual_class_dirs)]

        # Further filter out rows where the image file itself does not exist
        def check_file_exists(img_name):
            img_path = os.path.join(self.dataset_dir, img_name)
            if os.path.exists(img_path):
                return True
            # Fallback: if path doesn't exist AND img_name doesn't already end with .jpg,
            # try adding .jpg extension. (This logic is also in __getitem__)
            if not img_name.lower().endswith(".jpg"):
                if os.path.exists(img_path + ".jpg"):
                    return True
            return False

        initial_len = len(self.df)
        self.df = self.df[self.df[self.img_col].apply(check_file_exists)]
        if len(self.df) < initial_len:
            print(f"Warning: Removed {initial_len - len(self.df)} entries from the dataset because image files were not found on disk.")

        self.df = self.df.reset_index(drop=True)

        if has_labels:
            if "label" not in self.df.columns:
                raise ValueError("Train/val CSV must have a 'label' column.")
            self.labels = self.df["label"].values
        else:
            self.labels = None

        self.image_ids = self.df[self.img_col].values

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        img_name = self.image_ids[idx]
        img_path = os.path.join(self.dataset_dir, img_name)

        # Fallback: if path doesn't exist AND img_name doesn't already end with .jpg,
        # try adding .jpg extension.
        if not os.path.exists(img_path) and not img_name.lower().endswith(".jpg"):
            img_path = img_path + ".jpg"

        image = Image.open(img_path).convert("RGB")

        if self.transform:
            image = self.transform(image)

        if self.has_labels:
            return image, int(self.labels[idx])
        else:
            return image, img_name   # return id so we can build submission


