history         = {"train_loss": [], "train_acc": [], "val_loss": [], "val_acc": [], "val_f1": []}

print("\n🚀 Starting Training...\n")

for epoch in range(1, NUM_EPOCHS + 1):
    t0 = time.time()

    train_loss, train_acc = train_one_epoch(model, train_loader, criterion, optimizer)
    val_loss, val_acc, val_f1 = validate(model, val_loader, criterion)

    scheduler.step(val_acc)

    elapsed = time.time() - t0
    print(f"Epoch [{epoch:02d}/{NUM_EPOCHS}]  "
          f"Train Loss: {train_loss:.4f}  Train Acc: {train_acc:.4f}  |  "
          f"Val Loss: {val_loss:.4f}  Val Acc: {val_acc:.4f}  F1: {val_f1:.4f}  "
          f"({elapsed:.1f}s)")

    history["train_loss"].append(train_loss)
    history["train_acc"].append(train_acc)
    history["val_loss"].append(val_loss)
    history["val_acc"].append(val_acc)
    history["val_f1"].append(val_f1)

    # Save best model
    if val_acc > best_val_acc:
        best_val_acc   = val_acc
        patience_count = 0
        torch.save(model.state_dict(), CHECKPOINT)
        print(f"   💾 Saved best model  (val_acc={best_val_acc:.4f})")
    else:
        patience_count += 1
        if patience_count >= PATIENCE:
            print(f"\n⏹  Early stopping triggered after {epoch} epochs (no improvement for {PATIENCE} epochs).")
            break

print(f"\n✅ Training complete. Best Val Accuracy: {best_val_acc:.4f}")

