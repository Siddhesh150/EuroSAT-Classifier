def predict_with_tta(model, dataset_dir, csv_path, transform, n_aug=5):
    """
    Average predictions over n_aug augmented versions of each test image.
    Uses the same train_transforms for augmentation.
    """
    tta_transform = transforms.Compose([
        transforms.Resize((IMG_SIZE, IMG_SIZE)),
        transforms.RandomHorizontalFlip(),
        transforms.RandomRotation(10),
        transforms.ToTensor(),
        transforms.Normalize(MEAN, STD),
    ])

    df = pd.read_csv(csv_path)
    df.columns = [c.strip().lower() for c in df.columns]
    img_col = "image_id" if "image_id" in df.columns else "filename"

    model.eval()
    all_preds, all_ids = [], []

    for _, row in tqdm(df.iterrows(), total=len(df), desc="TTA Predict"):
        img_path = os.path.join(dataset_dir, row[img_col])
        if not os.path.exists(img_path):
            img_path += ".jpg"

        image = Image.open(img_path).convert("RGB")
        logits = torch.zeros(NUM_CLASSES).to(DEVICE)

        with torch.no_grad():
            # 1 pass with standard transform
            x = transform(image).unsqueeze(0).to(DEVICE)
            logits += model(x).squeeze(0)
            # n_aug-1 passes with augmentation
            for _ in range(n_aug - 1):
                x = tta_transform(image).unsqueeze(0).to(DEVICE)
                logits += model(x).squeeze(0)

        pred = logits.argmax().item()
        all_preds.append(pred)
        all_ids.append(row[img_col])

    return all_ids, all_preds


